using namespace std;
class smartQueue
{
    int *arr;      
    int capacity;  
    int first;      
    int rear;      
    int count;     
 
public:
    smartQueue();   
    ~smartQueue();  
 
    void pop();
    void push(int number);
    int front();
    int size();
    bool empty();
    int min();
    int max();

};
 
smartQueue::smartQueue()
{
    arr = new int[2147483647];
    capacity = 2147483647;
    first = 0;
    rear = -1;
    count = 0;
}

smartQueue::~smartQueue() {
    delete[] arr;
}

void smartQueue::pop()
{
    first = (first + 1) % capacity;
    count--;
}

void smartQueue::push(int number)
{
    rear = (rear + 1) % capacity;
    arr[rear] = number;
    count++;
}
 
int smartQueue::front()
{
    return arr[first];
}
 
int smartQueue::size() {
    return count;
}
 
bool smartQueue::empty() {
    if(size()==0){
       return true;
    }
    return false;
}
 
int smartQueue::max() {
    int maximum(arr[0]);
    for(int i =0; i<size();i++){
       if (arr[i]>maximum){
          maximum = arr[i];
       }
    }
    return maximum;
}
 
int smartQueue::min() {
    int minimum(arr[0]);
    for(int i =0; i<size();i++){
       if (arr[i]<minimum){
          minimum = arr[i];
       }
    }
    return minimum;
}
 

